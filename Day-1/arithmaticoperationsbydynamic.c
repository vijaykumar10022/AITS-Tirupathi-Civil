#include<stdio.h>
int main()
{
	int a,b;
	scanf("%d%d",&a,&b);
	printf("sum of %d and %d is: %d\n",a,b,a+b);
	printf("diff of %d and %d is: %d\n",a,b,a-b);
	printf("mul of %d and %d is: %d\n",a,b,a*b);
	printf("div of %d and %d is: %.1f\n",a,b,(float)a/b);
	printf("modulo div of %d and %d is: %d\n",a,b,a%b);
	return 0;
}

	//~ /(Division) --->quotient
	//~ %(modulo Division) --->reminder
	//~ 4/5-->0
	//~ 4/5-->4






